- (UIImage *)resizableImageWithImage{
    UIEdgeInsets insets = UIEdgeInsetsMake(self.size.height/2, self.size.width/2, self.size.height/2, self.size.width/2);
    UIImage * changedImage = [self resizableImageWithCapInsets:insets resizingMode:UIImageResizingModeStretch];
    return changedImage;
}

//图片拉伸
- (UIImage *)draughtImage{
    
    float resizableH = self.size.height / 2;
    
    float resizableW = self.size.width / 2;
    
    UIEdgeInsets edge = UIEdgeInsetsMake(resizableH, resizableW, resizableH, resizableW);
    
    //图片拉伸
    UIImage *resultImage = [self resizableImageWithCapInsets:edge];
    
    return resultImage;
}

//图片拉伸到指定大小
- (UIImage *)scaleImageToSize:(CGSize)newSize {
    
    CGRect scaledImageRect = CGRectZero;
    
    CGFloat aspectWidth = newSize.width / self.size.width;
    CGFloat aspectHeight = newSize.height / self.size.height;
    CGFloat aspectRatio = MAX ( aspectWidth, aspectHeight );
    
    scaledImageRect.size.width = self.size.width * aspectRatio;
    scaledImageRect.size.height = self.size.height * aspectRatio;
    scaledImageRect.origin.x = (newSize.width - scaledImageRect.size.width) / 2.0f;
    scaledImageRect.origin.y = (newSize.height - scaledImageRect.size.height) / 2.0f;
    
    UIGraphicsBeginImageContextWithOptions( newSize, NO, 0 );
    [self drawInRect:scaledImageRect];
    UIImage* scaledImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return scaledImage;
}


//图片识别人脸
- (UIImage *)imageCheckFace:(UIImage *)faceImage{
    UIImage * newImage = [faceImage copy];
    
    //此处是CIDetectorAccuracyHigh，若用于real-time的人脸检测，则用CIDetectorAccuracyLow，更快
    CIDetector *faceDetector = [CIDetector detectorOfType:CIDetectorTypeFace
                                                  context:nil
                                                  options:@{CIDetectorAccuracy: CIDetectorAccuracyHigh}];
    CIImage *ciimg = [CIImage imageWithCGImage:faceImage.CGImage];
    //得到面部数据
    NSArray *features = [faceDetector featuresInImage:ciimg];
    
    for (CIFaceFeature *f in features){
        CGRect aRect = f.bounds;
        NSLog(@"%f, %f, %f, %f", aRect.origin.x, aRect.origin.y, aRect.size.width, aRect.size.height);
        
        //眼睛和嘴的位置
        if(f.hasLeftEyePosition){
            NSLog(@"Left eye %g %g\n", f.leftEyePosition.x, f.leftEyePosition.y);
        }
        if(f.hasRightEyePosition){
            NSLog(@"Right eye %g %g\n", f.rightEyePosition.x, f.rightEyePosition.y);
        }
        if(f.hasMouthPosition){
            NSLog(@"Mouth %g %g\n", f.mouthPosition.x, f.mouthPosition.y);
        }
    }
    
    return newImage;
}

